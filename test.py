import numpy as np

# ダミーデータを生成
data = np.array([
    [28.1, 0.3, 1.2, 39.5, 1],
    [37.9, 0.2, 1.0, 31.2, 1],
    [28.1, 0.3, 1.2, 34.5, 0]
])

# データを入力と出力に分割
X = data[:, :-1]  # 入力: 温度, 潮の速さ, 潮の高さ, 塩分濃度
y = data[:, -1]   # 出力: 分類ラベル

# 各列ごとの最小値を求めて一次元配列に格納
min_values = X.min(axis=0)

# 結果を出力
print("各列ごとの最小値:", min_values)
print("最小値を持つ列のインデックス:", min_values.argmin())
